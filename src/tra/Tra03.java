package tra;
// 관계연산자 실습
public class Tra03 {
    public static void main(String[] args) {
        System.out.println( 3 == 4 ); //fasle
        System.out.println( 3 != 4 ); //true
        System.out.println( 3 > 4 ); //fasle
        System.out.println( 3 < 4 ); //true
        System.out.println( 3 >= 4 ); //fasle
        System.out.println( 3 <= 4 ); //true
        System.out.println( 3 == 3 && 4 == 3 ); //fasle
        System.out.println( 3 == 3 || 4 == 3 ); //true
    }
}
